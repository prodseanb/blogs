<h1 id="ctflearn">What is Artificial Intelligence?</h1>
<p>
In this article, we will explore the global impacts of <b>Artificial Intelligence</b> 
(AI) and answer some of the most controversial questions regarding its nature.
</p>

<h3>Let's get started</h3>
<p>
Artificial Intelligence is a branch of technology that aims to mimic human behavior through the automation of human tasks.
Professor Lon Appleby, a professor at <a href="https://durhamcollege.ca/">Durham College</a>,
stated that <b>the evolution of machines cannot be stopped</b>, 
similar to how the biggest force humans contend with is evolution. 
How do we deal with the rapid transformation of society caused by AI? 
Are we supposed to fear the changes, or can we utilize it to our advantage? 
And most importantly, what is the relationship between AI and environmental sustainability and how can we improve it? 
</p>

<br/><br/>
<h1>Machine Learning</h1>
<p><img src="https://user-images.githubusercontent.com/59718043/125829173-e83848e4-2e10-49fd-9b8d-05a18bc3038d.gif" alt="machine learning neural networks" /></p>
<p>
To delve in deeper into these questions, we must understand Artificial Intelligence. AI’s fundamental purpose is to learn. 
A machine’s learning process is comparable to a human brain. <b>Machine learning</b> is a process executed by a 
program that aims to learn as it operates without human intervention. Similar to the human brain, AI is made up of 
layers and connections. This network of connections is called the <b>deep neural network</b>. This neural 
network consists of everything a machine is programmed to learn from a set of data that is provided. As the machine progresses 
and learns, it provides an output of the compiled knowledge.
</p>
<h3>AI's role in medical research</h3>
<p>
A visual automation platform called <a href="https://deepomatic.com/en/">Deepomatic</a> collaborated with <a href="https://www.linkedin.com/company/lltech/about/">Light for Life Technologies</a> 
to develop an image recognition program that detects signs of <b>cancer</b>. This technology analyzes microscopic tissue images and 
performs its own diagnoses, proven to be as accurate as trained medical professionals. The way this technology learns is through a neural network. 
As it analyzes various images, its neural network expands. Hence, it  develops a better decision-making process and provides better 
findings.
</p>
<p><img src="https://user-images.githubusercontent.com/59718043/125831912-70074f65-19e6-4ccf-a9b3-28871784856f.png" alt="machine learning as a human" /></p>
<p>
Think of it like the <b>early stages of human life</b>. As we grow older, our perspectives expand as we learn new things. 
The more we learn, the better our judgment becomes, for most people at least. Comparing it to the model of machine learning, 
programmers and engineers have developed a technology that learns just like a human would. 
</p>

<br/><br/>
<h1>How do we deal with change?</h1>
<p>
As mentioned, Professor Lon Appleby stated that the biggest force humans contend with is evolution. The evolution of technology 
will only keep on going as we humans evolve. And as the universe decides to make our kind extinct, technology will stay, and our 
ideas will live on. Focusing on our current situation, how do we deal with AI’s rapid growth and development?
</p>
<h3>AI vs. Humans</h3>
<p>
There will come a time when technology replaces humans in the workplace. According to Oxford Economics, 20 million manufacturing 
jobs globally will be replaced by AI by 2030. The assembly speed will be much quicker and more accurate, and 
there will be no opportunities for human error in the production.
<br/><br/>
However, this results in <b>20 million jobs lost</b>. How do our governments and hierarchies adapt to that? A rough proposal would be 
for the government to provide a universal income, payment made to the entire population regardless of their status of employment. Or they could create new jobs 
that would incorporate humans and technology together in the workplace.
</p>
<p><img src="https://user-images.githubusercontent.com/59718043/125833693-450a2642-09c5-4e6a-96f7-d29af11a8309.jpg" alt="bots and humans in the workplace" /></p>
<p>
The unpredictability and uncertainty of our future circumstances with Artificial Intelligence is what's causing fear and doubt
about its nature and potential.
</p>
<br/><br/>
<h1>The fundamental nature of AI</h1>
<p>
Most of us do not realize that technology will only go as further as humans want it to go. It is fundamentally based on our own 
decisions. AI is centered on <b>human behavior</b>. The possibility of it becoming sentient depends on the decision of the developer. 
And the possibility of it killing us all is slim, unless it is allowed by humans.
</p>
<p><img src="https://user-images.githubusercontent.com/59718043/125836102-963d8e19-59d3-40ff-80f9-34d7791b3222.gif" alt="elmo pixel fire" /></p>
<p>
Professor Lon Appleby also mentioned that we could explore technology just like how we discovered many uses for fire. 
The discovery of fire was one of our very first steps to evolution. We are responsible for treating it either as a useful 
tool for crafting or as a weapon of destruction. This method of thinking should also be applied to how we explore technology. 
Are we going to develop useful tools or use it as a weapon to kill each other? It is all up to us.
</p>
<br/><br/>
<h1>Sustainability and Artificial Intelligence</h1>
<p>
Environmental sustainability is one of the main concerns with the rapid growth of technology. How can we incorporate technology 
and sustainability? 
</p>
<h3>Moving to the cloud</h3>
<p>
AI runs on large quantities of data on servers that depend on energy. According to a study in <a href="https://www.massachusetts.edu/">University of 
Massachusetts</a>, AI is regarded as one of the top carbon dioxide emitters in the world. However, we also can't ignore the 
positive impacts produced by AI such as <b>cloud platforms</b>. Amazon, Google, and Microsoft, some of the largest tech companies 
globally, run on massive quantities of data. These three companies have incorporated an AI to store a large portion of data into 
a cloud platform, an internet-based data center. This reduces the energy needed to power their large data centers. 
<br/><br/>
</p>
<p><img src="https://user-images.githubusercontent.com/59718043/125839596-142deca3-4df6-433e-870f-411c339d153e.png" alt="cloud" /></p>

<p>
In agriculture, AI has also helped farmers monitor crops and improve crop yields with the help of <b>drones</b> and 
<b>advanced sensors.</b> 
<br/><br/>
In manufacturing, AI has reduced production 
waste while robots provide <b>better assembly accuracy</b>.
<br/><br/> 
There are many other net positive impacts of AI on our 
environment that we should continue to develop and build upon. In conclusion, there is a hope for a more sustainable future 
with the help of Artificial Intelligence, we just need to find and implement a suitable balance. 
</p>

<br/><br/>
<h1>Conclusion</h1>
<p>
As Artificial Intelligence evolves, it will come with many challenges. We humans should be prepared to adapt to the changes 
and incorporate technology into our lives. It is our responsibility to use it as a tool for survival or convenience, 
and not as a weapon of destruction. 
There will come a time when the universe decides to banish us all, technology will survive, our ideas will live on.
</p>
